# Take a Number and check if it is Positive, Negative, or Zero.

def check_num(num1):
    if num1 > 0:
        print(f"{num1} is Positive")
    elif num1 < 0:
        print(f"{num1} is Negative")
    else:
        print(f"{num1} is Zero")
check_num(1) # 1 is Positive
check_num(-9) # -9 is Negative
check_num(0) # 0 is Zero

